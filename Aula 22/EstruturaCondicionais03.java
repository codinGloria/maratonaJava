package com.codingloria.maratonaJava.introducao;

public class EstruturaCondicionais03 {
    public static void main(String[] args) {

        double salario = 6000;

        // (condicao) ? verdadeiro : falso
        String resultado = salario > 5000 ? "Vou doar 500 reais" : "Ainda não tenho condições, mas vou ter!";

        System.out.println(resultado);
    }
}
